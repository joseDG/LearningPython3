'''
== - son iguales
!= - diferentes
> - mayor
< - menor
>= - mayor igual
<= - menor igual
'''

#Ejemplo
numero1 = float(input("Primer numero: "))
numero2 = float(input("Segundo numero: "))
numero3 = float(input("Tercero numero: "))
numero4 = float(input("Cuarto numero: "))

print("==", numero1 == numero2)
print("!=", numero2 != numero3)
print(">" , numero3 > numero4)
print("<" , numero2 < numero4)
print(">=", numero1 >= numero2)
print("<=", numero3 <= numero4)
